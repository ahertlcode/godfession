    public function __construct($inparams=array()) {
      if (!empty($inparams)) {
        if (isset($inparams['admin_roles_id']) && ($inparams['admin_roles_id'] != "")) $this->procarr['id'] = $this->id =  $inparams['admin_roles_id'];
